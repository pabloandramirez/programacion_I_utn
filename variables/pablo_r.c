#include <stdio.h>

int main() {
	int a;
	int b;
	char p;//practica de mostrar mensaje por pantalla
	
	printf("Hola Mundo");
	printf("\nPablo Ramirez");
	
	a = 2;
	printf("El valor de a es: %d", a);
	printf("\n");
	
	b = 15;
	printf("El valor de b es: %i", b);
	printf("\n");
	printf("\n");
	printf("El valor de a es %d y el valor de b es %d", a, b);
	printf("\n");
	printf("\n");
	p = 'r';
	printf("El valor del caracter p es %c", p);
	printf("\n");
	printf("++++++++++++++++++++++++++++++++++++");
	printf("\n");
	printf("Los valores ingresado son %d, %c y %i", b, p, a);
	printf("\n");
	printf("++++++++++++++++++++++++++++++++++++");
	
	return 0;
}
